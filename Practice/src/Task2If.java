import java.util.Scanner;

public class Task2If {
public static void main(String[] args) {
	/*  Allow user to enter grade and then provide explanation: A-
		 Excellent, B-Good,
		 *C-Average, D-Bad, any other grade --> Not Acceptable. At the end your program
		 *should print which grade was entered by a user with explanation.*/
	
	char grade;
	String a,b,c,d,expl;
	a="Exellent";
	b="Good";
	c="Average";
	d="Bad";
	Scanner input=new Scanner(System.in);
	System.out.println("What is your grade?");
	grade=input.next().charAt(0);
	
	if(grade=='A') {
		expl=a;
	}else if(grade=='B') {
		expl=b;
	}else if(grade=='C') {
		expl=c;
	}else if(grade=='D') {
		expl=d;
	}else {
		expl="Not Accepteble";
	}
	System.out.println("Your grade is "+grade+" and is "+expl+".");
	
}
}
